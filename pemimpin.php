<center><h2>Pemimpin adalah Pemimpi</h2></center>

<p>Sumber daya alami yang paling langka adalah kepemimpinan. Bukan kepemimpinan yang memperindah dirinya sendiri dengan segala kesibukan dan kedamaian 
	melakukan segala sesuatu, namun dengan hal-hal yang akan dapat membuat orang melakukan sesuatu yang mereka pikir mereka tidak dapat melakukan. Banyak 
	orang menganggap kepemimpinan adalah talenta, tetapi talenta tanpa kerja keras akan menjadi tragedi dimana kepemimpinan kita tidak berharga di mata 
	pengikut kita. Dalam masa sekarang, dibutuhkan pemimpin yang efektif dan handal. Napoleon Bonaparte berkata, "Kalau anda berperang, jangan melihat berapa 
	jumlah tentara musuh, tetapi lihatlah siapa jendralnya. "Dalam sebuah peperangan kita tidak perlu menakuti jumlah tentara musuh, cukup kita mengalahkan 
	jendralnya, maka kita akan memenangkan peperangan.</p>  

<p>Pemimpin dalam bahasa inggris disebut Leader yang berasal dari kataa Lead artinya "harus pergi." Jadi, pemimpinlah yang pertama kali harus pergi, karena 
	dia harus mencari sesuatu yang belum pernah diketahui anggota. Seorang pemimpin juga harus memiliki nilai tambah daripada anggotanya. Jika seorang anggota 
	memiliki nilai 10, maka seorang pemimpin harus memiliki nilai 10+, karena pemimpinlah yang akan membawa dan menentukan kemajuan organisasinya. Chris Cowney, 
	dalam bukunya "Heroic Leadership, Best Practices from a 450-year-old Company that Changed the World" (New York, 2003). Cowney mengatakan bahwa ada 4 konsep 
	kepemimpinan:</p>
<ol type="1">
	<li>Kita semua akan memimpin sepanjang waktu</li>
	<li>Kepemimpinan itu timbul dari dalam</li>
	<li>Kepemimpinan adalah cara hidup kita,</li>
	<li>Untuk menjadi pemimpin adalah proses pengembangan diri secara terus menerus.</li>
</ol>

<p>Kebesaran suatu organisasi berhubungan langsung dengan kebesaran pemimpinnya, sangat jarang kalau sebuah organisasi dapat berada lebih tinggi dari 
	pemimpinnya. Hal terbaik yang dapat dilakukan seorang pemimpin adalah mengembangkan pribadinya,  karena ketika pemimpin berkembang secara pribadi, 
	anggotanya akan meningkatkan kemampuannya untuk memimpin. Setelah mereka meningkatkan kemampuan memimpinnya, mereka akan memperbesar  kemampuan 
	organisasinya untuk bertumbuh.</p>
<p>Ada 10 langkah menjadi pemimpin berhasil:</p>
<ol type="1">
	<li>VISI:  Hal mutlak yang harus dimiliki</li>
	<li>PERUBAHAN:  Suatu tindakan untuk mencapai tujuan</li>
	<li>PENGARUH:  Suatu senjata yang ampuh </li>
	<li>KREATIVITAS:  Suatu langkah besar mencapai tujuan</li>
	<li>KARAKTER:  Suatu nilai berharga</li>
	<li>PENGATURAN WAKTU:  suatu pembuka keberhasilan</li>
	<li>MENGAMBIL KEPUTUSAN:  Suatu keberanian yang dibutuhkan</li> 
	<li>MENGATUR KONFLIK:  Suatu keahlian dalam keadaan kritis</li>
	<li>KESEHATAN:  Suatu modal penting yang perlu dijaga</li>
	<li>SUKSES:  Suatu persiapan untuk kesinambungan</li>
</ol>

<p>Menurut Warren Bennis dan Burt Nanus (1997) dalam bukunya "Leaders:  Strategis for Taking Charge" mengatakan bahwa ada lebih dari 850 definisi tentang 
	kepemimpinan. Dan jika anda menanyakan 10 orang, maka anda akan menemukan jawaban berbeda pada setiap orang tentang kepemimpinan.<p>
<ul type ="circle">
	<li>Membebaskan orang untuk melakukan apa yang dituntut dari mereka yang paling afektif dan manusiawi mungkin. (Max De Pree)</li>
	<li>Kepemimpinan adalah kemampuan untuk mendapatkan pengikut. (James C. Georges-ParTraining Corporation)</li>
	<li>Kepemimpinan adalah proses persuasi atau contoh di mana seorang individu (atau tim kepemimpinan) mendorong sebuah kelompok untuk mengejar 
		tujuan-tujuan yang dimiliki oleh seorang pemimpin atau bersama oleh pemimpin dan / pengikutnya. (John W. Gardner) </li>
	<li>Pemimpin afektif anggota tim memberikan kepercayaan diri untuk bertindak, untuk mengambil alih tanggung jawab mereka, dan membuat perubahan terjadi 
		bukan hanya melakukan tugas yang ditugaskan. Singkatnya pemimpin menciptakan pemimpin. (Carl E. Larsors dan Frank M. Lapasto)</li>
</ul>

