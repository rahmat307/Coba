	<center><h2>Kisah Ashabul Kahfi</h2></center>
<p>Kisah Ashabul Kahfi merupakan dua dari tiga kisah dalam surat al-Kahfi, sehingga digunakan sebagai nama surat dalam al-Qur’an. Kisah Ashabul Kahfi 
	diperkirakan terjadi pada pertengahan abad ke-3 pada masa kekaisaran Diqyanius yang merasa dirinya Tuhan dan memaksa rakyat untuk menyembah dirinya. 
	Hal ini tentu bukan hal semata, melainkan karena banyak pelajaran (ibrah) yang dapat diambil dari kisah tersebut.</p>
<p>Berkisah dari wafatnya seorang pemimpin yang adil dan arif di negeri Ephesus, seluruh warga merasa sangat sedih dan kehilangan. Wajah langit yang 
	sebelumnya amat panas, kini terlihat kelam oleh mendung, seakan ikut bersedih. Dan burung-burung gagak pun menggepakkan sayap-sayapnya, terbang 
	mengelilinggi kota Ephesus.</p>
<p>Sepeninggal ayahnya, Andalas bermimpi buruk tentang kaisar Diqyanius yang sedang menduduki singasana kerajaan Ephesus dan keadaan istana dibanjiri oleh 
	darah dan dinding istana runtuh. Negeri Ephesus yang baru berduka, Kini Ephesus sedang terancam malapetaka, karena kebenaran mimpi Andalas. Diqyanius 
	kaisar Romawi sedang haus kekuasaan berusaha menginvansi negeri tersebut. Negeri makmur, aman, damai nan sentosa itu, kini tengah di ujung kehancuran, 
	karena akan terjadi perang yang dahsyat.</p>
<p>Peperangan pun terjadi, Romawi menyerang Ephesus saat fajar menyingsing. Andalas segera mengerahkan serasker kerajaan untuk berperang. Terjadilah perang 
	yang sangat dahsyat antara bangsa Ephesus dengan bangsa Romawi . banyak prajurit yang gugur, bahkan warga yang tak bersalah pun menjadi kambing hitam 
	prajurit Romawi. Bau darah segar dimana-mana, bahkan Pangerang Andalas gugur di tangan Diqyanius setelah terjadi pergulatan antara mereka berdua. Perang 
	berakhir dengan sangat mengenaskan dan mengerikan. Negeri Ephesus pun dapat ditaklukan Romawi.</p>
<p>Romawi akhirnya menguasai Ephesus. Diqyanius sebagai kaisar Romawi mengorasikan dirinya sebagai Raja Ephesus dan menyuruh seluruh rakyat Ephesus untuk 
	mentaatinya dan barangsiapa yang mengingkarinya akan mendapatkan hukuman pancung, serta menyuruh mereka berpaling dari ajaran Nabi Isa A.S. dan menyembah 
	berhala Romawi. Waktu pun terus berlalu, banyak warga Ephesus yang mati karena tak sanggup menahan siksaan dari kaisar Diqyanius, sehingga Diqyanius 
	mengakui dirinya sebagai Tuhan yang mulia yang menentukan hidup-matinya warga Ephesus. Rakyat Ephesus pun terpaksa menyembahnya agar bertahan hidup. 
	Tidak bagi Tamlikha, Martelius, Sideminus, Casitius, Miksalimna, dan Mikhaslimina, ke-enam pemuda itu masih tetap dalam tauhid ajaran Nabi Isa A.S. meskipun 
	mereka mempunyai kedudukan tertinggi dalam istana sebagai pendamping kaisar Diqyanius, tetapi mereka tetap tidak mengakui Diqyanius sebagai tuhan.</p>

